# ==============================================================================
# Project: Prediction of Hip Fracture Risk in Older Chinese Adults (CHARLS Study)
# Script: Part 3 - Feature Screening and Missing Value Handling (KNN Imputation)
# Purpose: Filter high-missingness variables, remove redundant features, 
#          and perform K-Nearest Neighbors (KNN) imputation.
# ==============================================================================

# 1. Load required libraries
if (!require(VIM)) install.packages("VIM")
if (!require(haven)) install.packages("haven")

library(VIM)
library(dplyr)
library(haven)

# ------------------------------------------------------------------------------
# 2. Preliminary Feature Screening based on Missing Rates
# ------------------------------------------------------------------------------

# Calculate missingness rate for each variable
missing_rates <- sapply(matched_data, function(x) sum(is.na(x)) / nrow(matched_data))

# Define a threshold of 50% for variable inclusion
valid_vars <- names(missing_rates[missing_rates <= 0.5])

# Subset the data to retain only variables meeting the threshold
matched_data_filtered <- matched_data[, valid_vars]

cat("--- Summary of Initial Feature Filtering ---\n")
cat("Original variables (including matching intermediates):", ncol(matched_data), "\n")
cat("Variables retained (missingness <= 50%):", ncol(matched_data_filtered), "\n")

# ------------------------------------------------------------------------------
# 3. Removal of Non-predictive and Administrative Variables
# ------------------------------------------------------------------------------
# Remove system IDs, wave indicators, survey timestamps, 
# and intermediate variables generated by the MatchIt package.

vars_to_remove <- c("ID", "wave", "householdID", "communityID", "iwy", "iwm", 
                    "distance", "weights", "subclass")

# Ensure only existing variables are selected for removal to avoid errors
vars_to_remove <- intersect(vars_to_remove, colnames(matched_data_filtered))

matched_data_final <- matched_data_filtered %>%
  select(-all_of(vars_to_remove))

cat("Variables remaining after removing administrative/redundant features:", ncol(matched_data_final), "\n")

# Identify columns requiring imputation
cols_with_na <- sum(sapply(matched_data_final, function(x) any(is.na(x))))
cat("Number of variables requiring KNN imputation:", cols_with_na, "\n")

# ------------------------------------------------------------------------------
# 4. Data Pre-processing for KNN Imputation
# ------------------------------------------------------------------------------

# 1) Exclude non-numeric/text variables that cannot be used in distance calculation
text_vars_to_remove <- c("province", "city", "nation")
text_vars_to_remove <- intersect(text_vars_to_remove, colnames(matched_data_final))

data_clean_for_knn <- matched_data_final %>%
  select(-all_of(text_vars_to_remove))

# 2) Strip Stata labels (haven attributes) and coerce all columns to numeric
# Note: zap_labels() is critical for processing CHARLS .dta files to avoid attribute errors.
data_numeric <- data_clean_for_knn %>%
  haven::zap_labels() %>% 
  mutate(across(everything(), as.numeric))

# 3) Handle columns that result in 100% NA after coercion (potential hidden text columns)
na_props <- colMeans(is.na(data_numeric))
bad_cols <- names(na_props[na_props == 1])

if(length(bad_cols) > 0) {
  cat("Warning: The following columns were converted to 100% NA and will be removed:", bad_cols, "\n")
  data_numeric <- data_numeric %>% select(-all_of(bad_cols))
} else {
  cat("Numeric conversion successful. No 100% NA columns detected.\n")
}

# ------------------------------------------------------------------------------
# 5. K-Nearest Neighbors (KNN) Imputation
# ------------------------------------------------------------------------------
# KNN replaces missing values with the mean/mode of the 'k' most similar neighbors.
# This approach preserves the covariance structure of the data better than mean imputation.



cat("Starting KNN imputation (k=5), please wait...\n")
set.seed(2027) # For reproducibility
data_imputed <- VIM::kNN(data_numeric, k = 5, imp_var = FALSE)

# Verify imputation results
remaining_nas <- sum(is.na(data_imputed))
cat("\n--- KNN Imputation Completed ---\n")
cat("Total missing values (NAs) remaining in the dataset:", remaining_nas, "\n")

# ------------------------------------------------------------------------------
# 6. Preparation of Final Modeling Dataset
# ------------------------------------------------------------------------------
# Rename the outcome variable and move it to the first column for clarity.

final_model_data <- data_imputed %>%
  rename(Hip_fracture = hip) %>%
  select(Hip_fracture, everything())

cat("\n--- Final Dataset for Modeling ---\n")
cat("Total Observations (Rows):", nrow(final_model_data), "\n")
cat("Total Features (Columns, including Outcome):", ncol(final_model_data), "\n")

